  @Prop() menu?: string[]
  private isModalVisible = false
  async clickimg(id) {
    const a = this.$store.getters['modules/reservationGourmet/getApplicationTO']
    const param = {
      id,
      query: {
        pickUpDate: a.pickUpDate,
        pickUpTime: a.pickUpTime
      }
    }
